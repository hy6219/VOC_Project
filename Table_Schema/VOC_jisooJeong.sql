DROP TABLE IF EXISTS `DRIVER`;

CREATE TABLE `DRIVER` (
	`DRIVER_ID`	BIGINT	NOT NULL,
	`DRIVER_NAME`	VARCHAR(2000)	NULL,
	`DRIVER_TEL`	VARCHAR(2000)	NULL,
	`DRIVER_ADDR`	VARCHAR(2000)	NULL,
