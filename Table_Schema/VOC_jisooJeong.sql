DROP TABLE IF EXISTS `CLAIM`;

CREATE TABLE `CLAIM` (
	`CLAIM_ID`	BIGINT	NOT NULL,
	`CLAIM_TYPE`	INT	NULL,
	`CLAIM_CONTENT`	VARCHAR(2000)	NULL,
	`CLAIM_REIMBURSE`	VARCHAR(5)	NULL,
	`BUSINESS_ID`	BIGINT	NOT NULL,
	`RETAIL_ID`	BIGINT	NOT NULL,
	`DRIVER_ID`	BIGINT	NOT NULL
);

DROP TABLE IF EXISTS `BUSINESS`;

CREATE TABLE `BUSINESS` (
	`BUSINESS_ID`	BIGINT	NOT NULL,
	`BUSINESS_CODE`	VARCHAR(2000)	NULL,
	`BUSINESS_NAME`	VARCHAR(2000)	NULL,
	`BUSINESS_TEL`	VARCHAR(2000)	NULL,
	`BUSINESS_ADDR`	VARCHAR(2000)	NULL
);

DROP TABLE IF EXISTS `PRODUCT`;

CREATE TABLE `PRODUCT` (
	`PRODUCT_ID`	BIGINT	NOT NULL,
	`PRODUCT_TYPE`	INT	NULL,
	`PRODUCT_NAME`	VARCHAR(2000)	NULL,
	`PRODUCT_CODE`	VARCHAR(2000)	NULL,
	`PRODUCT_RECDATE`	TIMESTAMP	NULL,
	`PRODUCT_DEPOSIT_CHK`	VARCHAR(5)	NULL,
	`BUSINESS_ID`	BIGINT	NOT NULL
);

DROP TABLE IF EXISTS `RETAIL`;

