	`DRIVER_PCHK`	VARCHAR(5)	NULL,
	`DRIVER_DISAGREE`	VARCHAR(5)	NULL,
	`VOC_REGDATE`	TIMESTAMP	NULL,
	`VOC_RECENT`	TIMESTAMP	NULL,
	`EID`	BIGINT	NOT NULL,
	`CLAIM_ID`	BIGINT	NOT NULL,
	`DRIVER_ID`	BIGINT	NOT NULL,
	`RETAIL_ID`	BIGINT	NOT NULL
);

DROP TABLE IF EXISTS `CS`;

CREATE TABLE `CS` (
	`EID`	BIGINT	NOT NULL,
